  <script>
    $(document).ready(function(){
      var limit = 3;
      var start = 0;
      var action = 'inactive';
      function load_data(limit, start){

          $.ajax({
            url:"api/v1/fetch.php",
            method:"POST",
            data:{limit:limit, start:start},
            cache:false,
            success:function(data)
            {
                $('#load_data').append(data);
                if(data == '')
                {
                    $('#load_data_message').html("<div><button style='width:100%' class='btn deep-purple'>No Data Found</button></div>");
                    action = 'active';
                }
                else
                {
                    $('#load_data_message').html("<div><button style='width:100%' class='btn deep-purple'>Loading, Please Wait...</button></div>");
                    action = 'inactive';

                }
            }
          })
      }

      if(action == 'inactive')
      {
        action = 'active';
        load_data(limit, start);
      }

      $(window).scroll(function(){
        if($(window).scrollTop() + $(window).height() > $("#load_data").height() && action == 'inactive') 
        {
          //alert("ya");
          action = 'active';
          start = start + limit;
          setTimeout(function(){
            load_data(limit,start);
          },2000);
        }
      });

    });
  </script>
  
